#include "car_motion.h"

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车前进
// @param        void    
// @return       void
// Sample usage:        car_forward(); //小车前进
//-----------------------------------------------------------------------------------------------------------------
void car_forward(void)
{
    l9110s_forward(left, 10000);
    l9110s_forward(right, 10000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车后退
// @param        void    
// @return       void
// Sample usage:        car_back(); //小车后退
//-----------------------------------------------------------------------------------------------------------------
void car_backward(void)
{
    l9110s_backward(left, 10000);
    l9110s_backward(right, 10000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车停止
// @param        void    
// @return       void
// Sample usage:        car_stop(); //小车停止
//-----------------------------------------------------------------------------------------------------------------
void car_stop(void)
{
    l9110s_forward(left, 0);
    l9110s_forward(right, 0);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车左转
// @param        void    
// @return       void
// Sample usage:        car_turnleft(); //小车左转
//-----------------------------------------------------------------------------------------------------------------
void car_turnleft(void)
{
    l9110s_forward(left, 5000);
    l9110s_forward(right, 10000);
}

//-------------------------------------------------------------------------------------------------------------------
// @brief        小车右转
// @param        void    
// @return       void
// Sample usage:        car_turnright(); //小车右转
//-----------------------------------------------------------------------------------------------------------------
void car_turnright(void)
{
    l9110s_forward(left, 10000);
    l9110s_forward(right, 5000);
}
void car_turnright1(void)
{
    l9110s_forward(left, 3500);
    l9110s_backward(right, 3500);
}
void car_turnleft1(void)
{
    l9110s_forward(right, 3500);
    l9110s_backward(left, 3500);
}

